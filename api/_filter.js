const CAT_CN = { porn: '色情低俗', violence: '暴力血腥', politics: '政治敏感' };

// 检查文本是否含违规内容
// 返回 { ok:true } 或 { ok:false, category:'色情低俗', hit:'命中词' }
export function checkContent(text = '') {
  const t = String(text).toLowerCase().replace(/\s+/g, '');
  for (const [cat, words] of Object.entries(BLOCK)) {
    for (const w of words) {
      if (w && t.includes(w.toLowerCase())) {
        return { ok: false, category: CAT_CN[cat], hit: w };
      }
    }
  }
  return { ok: true };
}
